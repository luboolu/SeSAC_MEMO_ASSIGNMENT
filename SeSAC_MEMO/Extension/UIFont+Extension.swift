import Foundation
import UIKit

extension UIFont {
    var binggrae: UIFont {
        return UIFont(name: "Binggrae", size: 13)!
    }
    
    var binggraeMedium: UIFont {
        return UIFont(name: "Binggrae", size: 17)!
    }
    
    var binggraeBoldSmall: UIFont {
        return UIFont(name: "Binggrae-Bold", size: 15)!
    }
    
    var binggraeBold: UIFont {
        return UIFont(name: "Binggrae-Bold", size: 25)!
    }
    
    var binggraeBoldLarge: UIFont {
        return UIFont(name: "Binggrae-Bold", size: 30)!
    }
}

